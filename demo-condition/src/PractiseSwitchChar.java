public class PractiseSwitchChar {
  public static void main(String[] args) {
    // Switch statement with char

    char tier = 'P';

    switch (tier) {
      case 'C':
        System.out.println("Classic card");
        break;
      case 'G':
        System.out.println("Gold card");
        break;
      case 'P':
        System.out.println("Platinum card");
        break;
      default:
    }

  }
}
